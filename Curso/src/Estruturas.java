public class Estruturas {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("Número: " + i);
        }




        int contador = 0;
        while (contador < 5) {
            System.out.println("Número: " + contador);
            contador++;
        }

    }
}